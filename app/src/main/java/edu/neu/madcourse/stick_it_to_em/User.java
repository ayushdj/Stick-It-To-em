package edu.neu.madcourse.stick_it_to_em;

public class User {
    String email;
    String full_name;
    String username;
    String sCount1;
    String sCount2;
    String sCount3;
    String sCount4;

    public User (String email, String full_name, String username, String sCount1, String sCount2,
                 String sCount3, String sCount4) {
        this.email = email;
        this.full_name = full_name;
        this.username = username;
        this.sCount1 = sCount1;
        this.sCount2 = sCount2;
        this.sCount3 = sCount3;
        this.sCount4 = sCount4;
    }
}
